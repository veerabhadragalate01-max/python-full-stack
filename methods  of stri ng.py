text= " You are going to hyderabad "

print(text.upper())# coversion smaller to capital
print(text.lower())# conversion of capital to smaller
print(text.title())# just first letter capital in every word
print(text.capitalize())# just first letter should be capital
print(text.swapcase())# all lower got coverted to upper and vice versa
print(text.find("are"))#finding the word 
print(text.replace("are","is"))#replace
print(text.count("a"))#counting no of varibles
print(text[:3])#slicing
print(text.lstrip())#right strip# remoing white space
print(text.rstrip())#left strip 
print(text.split(" "))
